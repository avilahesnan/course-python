# 2. Faça um programa que peça para o usuário digitar três valores inteiro e imprima a soma deles.
num1: int = int(input('Digite o primeiro número inteiro '))
num2: int = int(input('Digite o segundo número inteiro '))
num3: int = int(input('Digite o terceiro número inteiro '))

soma1: int = num1 + num2 + num3
print(soma1)

# 3. Faça um programa que recebe três valores e apresente a soma dos quadrados dos valores lidos.
numero1: int = int(input('Digite o primeiro número inteiro '))
numero2: int = int(input('Digite o segundo número inteiro '))
numero3: int = int(input('Digite o terceiro número inteiro '))

soma2: int = (numero1 * numero1) + (numero2 * numero2) + (numero3 * numero3)
print(soma2)
