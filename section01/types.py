"""
Tipo "str" é também conhecido como String

    "Hello World"

Tipo "int" é também conhecido como numérico

    10

Tipo "float" é também conhecido como ponto flutuante

    10.5

Tipo "complex" são os números complexos

    5j

Tipo "bool" é também conhecido como Booleano

    True -> Verdadeiro
    False -> Falso
"""

exemplo_1 = 'Hello World'
print(exemplo_1)
print(type(exemplo_1))

exemplo_2 = 10
print(exemplo_2)
print(type(exemplo_2))

exemplo_3 = 10.5
print(exemplo_3)
print(type(exemplo_3))

exemplo_4 = 5j
print(exemplo_4)
print(type(exemplo_4))

exemplo_5 = True
print(exemplo_5)
print(type(exemplo_5))
